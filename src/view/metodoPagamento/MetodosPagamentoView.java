    public void readTable() throws SQLException{
        DefaultTableModel model = (DefaultTableModel) metodoPagamentoTable.getModel();
        model.setNumRows(0);
        MetodoPagamentoDAO metodoPagamentoDAO = new MetodoPagamentoDAO();
        
        for(MetodoPagamento c : metodoPagamentoDAO.listAllMetodoPagamentos()){
            model.addRow(new Object[]{
                c.getMetodo_pagamento_id(),
                c.getNome()
            });
        }
    }
    
    public void readSearchTableByName(String name) throws SQLException{
        DefaultTableModel model = (DefaultTableModel) metodoPagamentoTable.getModel();
        model.setNumRows(0);
        MetodoPagamentoDAO categoriaDAO = new MetodoPagamentoDAO();
        
        for(MetodoPagamento c : categoriaDAO.searchMetodoPagamentoByName(name)){
            model.addRow(new Object[]{
                c.getMetodo_pagamento_id(),
                c.getNome()
            });
        }
    }
    
    public void readSearchTableById(int id) throws SQLException{
        DefaultTableModel model = (DefaultTableModel) metodoPagamentoTable.getModel();
        model.setNumRows(0);
        MetodoPagamentoDAO categoriaDAO = new MetodoPagamentoDAO();
        
        for(MetodoPagamento c : categoriaDAO.searchMetodoPagamentoById(id)){
            model.addRow(new Object[]{
                c.getMetodo_pagamento_id(),
                c.getNome()
            });
        }
    }

