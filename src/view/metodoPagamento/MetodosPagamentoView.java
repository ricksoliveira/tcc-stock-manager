    private void buttonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCadastrarActionPerformed
        
        if(metodoPagamentoNameTxt.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Erro! MetodoPagamento sem nome.\nPor favor escolha um nome para a metodoPagamento.");
        }
        else{
            
            int confirm = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja cadastrar este método de pagamento?", "Atenção", JOptionPane.YES_NO_OPTION);
            if(confirm == JOptionPane.YES_OPTION){
                try {
                    MetodoPagamento metodoPagamento = new MetodoPagamento();
                    MetodoPagamentoDAO metodoPagamentoDAO = new MetodoPagamentoDAO();

                    metodoPagamento.setNome(metodoPagamentoNameTxt.getText());
                    metodoPagamentoDAO.createMetodoPagamento(metodoPagamento);

                    metodoPagamentoNameTxt.setText("");

                    this.readTable();

                } catch (SQLException ex) {
                    Logger.getLogger(MetodosPagamentoView.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            
        }
        
        
        
    }//GEN-LAST:event_buttonCadastrarActionPerformed

