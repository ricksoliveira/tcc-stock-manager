    public void readDeletedTable() throws SQLException{
        DefaultTableModel model = (DefaultTableModel) metodoPagamentoExcluidoTable.getModel();
        model.setNumRows(0);
        MetodoPagamentoDAO metodoPagamentoDAO = new MetodoPagamentoDAO();
        
        for(MetodoPagamento c : metodoPagamentoDAO.listDeletedMetodoPagamentos()){
            model.addRow(new Object[]{
                c.getMetodo_pagamento_id(),
                c.getNome()
            });
        }
    }
    
    public void readDeletedSearchTableByName(String name) throws SQLException{
        DefaultTableModel model = (DefaultTableModel) metodoPagamentoExcluidoTable.getModel();
        model.setNumRows(0);
        MetodoPagamentoDAO categoriaDAO = new MetodoPagamentoDAO();
        
        for(MetodoPagamento c : categoriaDAO.searchDeletedMetodoPagamentoByName(name)){
            model.addRow(new Object[]{
                c.getMetodo_pagamento_id(),
                c.getNome()
            });
        }
    }
    
    public void readDeletedSearchTableById(int id) throws SQLException{
        DefaultTableModel model = (DefaultTableModel) metodoPagamentoExcluidoTable.getModel();
        model.setNumRows(0);
        MetodoPagamentoDAO categoriaDAO = new MetodoPagamentoDAO();
        
        for(MetodoPagamento c : categoriaDAO.searchDeletedMetodoPagamentoById(id)){
            model.addRow(new Object[]{
                c.getMetodo_pagamento_id(),
                c.getNome()
            });
        }
    }

