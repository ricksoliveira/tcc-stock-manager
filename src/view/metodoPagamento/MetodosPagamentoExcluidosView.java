    private void buttonRestaurar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonRestaurar1ActionPerformed

        if(metodoPagamentoExcluidoTable.getSelectedRow() != -1){
            
            int confirm = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja restaurar este método de pagamento?", "Atenção", JOptionPane.YES_NO_OPTION);
            if(confirm == JOptionPane.YES_OPTION){
                try {
                    MetodoPagamento metodoPagamento = new MetodoPagamento();
                    MetodoPagamentoDAO metodoPagamentoDAO = new MetodoPagamentoDAO();

                    metodoPagamento.setMetodo_pagamento_id((int) metodoPagamentoExcluidoTable.getValueAt(metodoPagamentoExcluidoTable.getSelectedRow(), 0));
                    metodoPagamento.setNome(metodoPagamentoExcluidoTable.getValueAt(metodoPagamentoExcluidoTable.getSelectedRow(), 1).toString());
                    metodoPagamentoDAO.restoreMetodoPagamento(metodoPagamento);

                    this.readDeletedTable();

                } catch (SQLException ex) {
                    Logger.getLogger(MetodosPagamentoView.class.getName()).log(Level.SEVERE, null, ex);
                }
            } 
            
        }
    }//GEN-LAST:event_buttonRestaurar1ActionPerformed

