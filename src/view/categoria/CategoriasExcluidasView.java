    public void readDeletedTable() throws SQLException{
        DefaultTableModel model = (DefaultTableModel) categoriaExcluidaTable.getModel();
        model.setNumRows(0);
        CategoriaDAO categoriaDAO = new CategoriaDAO();
        
        for(Categoria c : categoriaDAO.listDeletedCategorias()){
            model.addRow(new Object[]{
                c.getCategoria_id(),
                c.getNome()
            });
        }
    }
    
    public void readDeletedSearchTableByName(String name) throws SQLException{
        DefaultTableModel model = (DefaultTableModel) categoriaExcluidaTable.getModel();
        model.setNumRows(0);
        CategoriaDAO categoriaDAO = new CategoriaDAO();
        
        for(Categoria c : categoriaDAO.searchDeletedCategoriaByName(name)){
            model.addRow(new Object[]{
                c.getCategoria_id(),
                c.getNome()
            });
        }
    }
    
    public void readDeletedSearchTableById(int id) throws SQLException{
        DefaultTableModel model = (DefaultTableModel) categoriaExcluidaTable.getModel();
        model.setNumRows(0);
        CategoriaDAO categoriaDAO = new CategoriaDAO();
        
        for(Categoria c : categoriaDAO.searchDeletedCategoriaById(id)){
            model.addRow(new Object[]{
                c.getCategoria_id(),
                c.getNome()
            });
        }
    }

