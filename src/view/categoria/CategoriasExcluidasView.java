    private void buttonRestaurarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonRestaurarActionPerformed

        if(categoriaExcluidaTable.getSelectedRow() != -1){
            try {
                Categoria categoria = new Categoria();
                CategoriaDAO categoriaDAO = new CategoriaDAO();

                categoria.setCategoria_id((int) categoriaExcluidaTable.getValueAt(categoriaExcluidaTable.getSelectedRow(), 0));
                categoria.setNome(categoriaExcluidaTable.getValueAt(categoriaExcluidaTable.getSelectedRow(), 1).toString());
                categoriaDAO.restoreCategoria(categoria);

                this.readDeletedTable();

            } catch (SQLException ex) {
                Logger.getLogger(CategoriasView.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_buttonRestaurarActionPerformed

