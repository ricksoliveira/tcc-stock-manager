    public void readDeletedTable() throws SQLException{
        DefaultTableModel model = (DefaultTableModel) promoExcluidaTable.getModel();
        model.setNumRows(0);
        PromoDAO promoDAO = new PromoDAO();
        
        for(Promo c : promoDAO.listDeletedPromos()){
            model.addRow(new Object[]{
                c.getPromo_id(),
                c.getNome(),
                c.getValor_desconto()
            });
        }
    }
    
    public void readDeletedSearchTableByName(String name) throws SQLException{
        DefaultTableModel model = (DefaultTableModel) promoExcluidaTable.getModel();
        model.setNumRows(0);
        PromoDAO promoDAO = new PromoDAO();
        
        for(Promo c : promoDAO.searchDeletedPromoByName(name)){
            model.addRow(new Object[]{
                c.getPromo_id(),
                c.getNome()
            });
        }
    }
    
    public void readDeletedSearchTableById(int id) throws SQLException{
        DefaultTableModel model = (DefaultTableModel) promoExcluidaTable.getModel();
        model.setNumRows(0);
        PromoDAO promoDAO = new PromoDAO();
        
        for(Promo c : promoDAO.searchDeletedPromoById(id)){
            model.addRow(new Object[]{
                c.getPromo_id(),
                c.getNome()
            });
        }
    }

