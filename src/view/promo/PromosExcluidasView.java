    private void buttonRestaurarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonRestaurarActionPerformed

        if(promoExcluidaTable.getSelectedRow() != -1){
            
            int confirm = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja restaurar esta promoção?", "Atenção", JOptionPane.YES_NO_OPTION);
            if(confirm == JOptionPane.YES_OPTION){
                try {
                    Promo promo = new Promo();
                    PromoDAO promoDAO = new PromoDAO();

                    promo.setPromo_id((int) promoExcluidaTable.getValueAt(promoExcluidaTable.getSelectedRow(), 0));
                    promo.setNome(promoExcluidaTable.getValueAt(promoExcluidaTable.getSelectedRow(), 1).toString());
                    promo.setValor_desconto((double) promoExcluidaTable.getValueAt(promoExcluidaTable.getSelectedRow(), 2));
                    promoDAO.restorePromo(promo);

                    this.readDeletedTable();

                } catch (SQLException ex) {
                    Logger.getLogger(PromosView.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            
            
        }

    }//GEN-LAST:event_buttonRestaurarActionPerformed

