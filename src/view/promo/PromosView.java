    private void buttonAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAtualizarActionPerformed
        
        if(promoUpdateNameTxt.getText().equals("") || promoUpdateDiscountValue.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Erro! Promo sem nome ou valor.\nPor favor preencha todos os campos para a promo.");
        }
        else{
            if(promoTable.getSelectedRow() != -1){
                
                int confirm = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja atualizar esta promoção?", "Atenção", JOptionPane.YES_NO_OPTION);
                if(confirm == JOptionPane.YES_OPTION){
                    try {
                        Promo promo = new Promo();
                        PromoDAO promoDAO = new PromoDAO();

                        promo.setPromo_id((int) promoTable.getValueAt(promoTable.getSelectedRow(), 0));
                        promo.setNome(promoUpdateNameTxt.getText());
                        promo.setValor_desconto(Double.parseDouble(promoUpdateDiscountValue.getText()));
                        promoDAO.updatePromo(promo);

                        promoUpdateNameTxt.setText("");
                        promoUpdateDiscountValue.setText("");

                        this.readTable();

                    } catch (SQLException ex) {
                        Logger.getLogger(PromosView.class.getName()).log(Level.SEVERE, null, ex);
                    } 
                }
            }
        }
        
        
        
    }//GEN-LAST:event_buttonAtualizarActionPerformed

