    public void readTable() throws SQLException{
        
        DefaultTableModel model = (DefaultTableModel) promoTable.getModel();
        
        model.setNumRows(0);
        
        PromoDAO promoDAO = new PromoDAO();
        
        for(Promo p : promoDAO.listAllPromos()){
            model.addRow(new Object[]{
                p.getPromo_id(),
                p.getNome(),
                p.getValor_desconto()
            });
        }
    }
    
    public void readSearchTableByName(String name) throws SQLException{
        DefaultTableModel model = (DefaultTableModel) promoTable.getModel();
        model.setNumRows(0);
        PromoDAO promoDAO = new PromoDAO();
        
        for(Promo c : promoDAO.searchPromoByName(name)){
            model.addRow(new Object[]{
                c.getPromo_id(),
                c.getNome()
            });
        }
    }
    
    public void readSearchTableById(int id) throws SQLException{
        DefaultTableModel model = (DefaultTableModel) promoTable.getModel();
        model.setNumRows(0);
        PromoDAO promoDAO = new PromoDAO();
        
        for(Promo c : promoDAO.searchPromoById(id)){
            model.addRow(new Object[]{
                c.getPromo_id(),
                c.getNome()
            });
        }
    }

