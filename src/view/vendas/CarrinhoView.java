    private void buttonRetirarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonRetirarActionPerformed
        
        if(carrinhoTable.getSelectedRow() != -1){
            try {
                ((DefaultTableModel)carrinhoTable.getModel()).removeRow(carrinhoTable.getSelectedRow());
                this.calculoTotal();
            } catch (SQLException ex) {
                Logger.getLogger(CarrinhoView.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Por favor selecione uma venda.");
        }
        
    }//GEN-LAST:event_buttonRetirarActionPerformed

    private void buttonAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAdicionarActionPerformed
        
        try {
            ProdutoDAO produtoDAO = new ProdutoDAO();
            
            if(produtoDAO.authProduto(codigoTxt.getText())){
                if((Integer) qtdeNum.getValue() <= 0){
                    JOptionPane.showMessageDialog(null, "Erro!" + "\n" + "O produto não pode ter quantidade 0 ou menor.");
                }
                else{
                    Produto produto = produtoDAO.getProdutoByCodigoInterno(codigoTxt.getText());
                    this.readCarrinhoTable(produto);
                    codigoTxt.setText("");
                    qtdeNum.setValue(0);
                    this.calculoTotal();
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Erro!" + "\n" + "Produto não encontrado.");
            }
        } catch (SQLException ex) {
            Logger.getLogger(CarrinhoView.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_buttonAdicionarActionPerformed

