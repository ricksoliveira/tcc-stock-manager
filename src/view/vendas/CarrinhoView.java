    public void calculoTotal() throws SQLException{
        
        double totalParcial = 0.0;
        double desconto = 0.0;
        
        PromoDAO promoDAO = new PromoDAO();
        desconto = promoDAO.getValorDesconto(venda.getPromo());
        
        for(int i = 0; i < carrinhoTable.getRowCount(); i++){
            totalParcial += Double.parseDouble(carrinhoTable.getValueAt(i, 5).toString());
        }
        
        totalParcialTxt.setText(String.format("%,.2f", totalParcial));
        descontoTxt.setText(String.format("%,.2f", totalParcial * desconto));
        totalTxt.setText(String.format("%,.2f", totalParcial - (totalParcial * desconto)));
    }
    
    public void readCarrinhoTable(Produto produto) throws SQLException{
        
        
        
        DefaultTableModel model = (DefaultTableModel) carrinhoTable.getModel();
        
        Object[] produtoCarrinho = {
            produto.getCod_interno(),
            produto.getProduto_id(),
            venda.getVenda_id(),
            produto.getPreco_unitario(),
            (Integer) qtdeNum.getValue(),
            (double) (produto.getPreco_unitario() * (Integer) qtdeNum.getValue())
        };
        
        model.addRow(produtoCarrinho);
        
    }

